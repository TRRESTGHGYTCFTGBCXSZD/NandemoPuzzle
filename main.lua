	end
end
function love.update(dt)
frames = frames + (dt*60)
	while frames > 1 do
		frameticks = frameticks + 1
		if moveuuu then 
			move = move + 1
			love.audio.stop(selectionselect)
			love.audio.play(selectionselect)
		end
		if moveddd then
			move = move - 1
			love.audio.stop(selectionselect)
			love.audio.play(selectionselect)
		end
		if coindexter >= 1 then
			coins = coins + coindexter
			coindexter = 0 
			love.audio.stop(coined)
			love.audio.play(coined)
		end
		frames = frames - 1
		local drooow = ""
		if coins > 1 then
		drooow = "CREDITS "
		else
		drooow = "CREDIT "
		end
		if coins <= 9 then
		drooow = drooow .. "0" .. coins
		else
		drooow = drooow .. coins
		end
		statbarbotcent:set(drooow)
	end
end
function drawsprite(image,x,y,cx,cy,sx,sy,rt)
